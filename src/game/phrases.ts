const RECOVERY = ['ДОГОНЯЙ'];

let lastPick = '';
const pickNoRepeat = (pool: string[]): string => {
  let p = pool[Math.floor(Math.random() * pool.length)];
  if (pool.length > 1) {
    let guard = 0;
    while (p === lastPick && guard++ < 6) p = pool[Math.floor(Math.random() * pool.length)];
  }
  lastPick = p;
  return p;
};

/** Фраза для майлстоуна комбо — тир по величине комбо. */
export function comboPhrase(combo: number): string {
  const t = combo >= 30 ? 3 : combo >= 20 ? 2 : combo >= 10 ? 1 : 0;
  return pickNoRepeat(TIERS[t]);
}

/** Фраза для возврата после промаха. */
export function recoveryPhrase(): string {
  return RECOVERY[Math.floor(Math.random() * RECOVERY.length)];
}
